	//更新标记
	public $_update = false;

	/*
	 * 用户ID,活动类型ID,活动ID
	 */
	public function __construct($uid,$hid = 1)
	{
		$this->uid = intval($uid);

		if (empty($this->atype)){
			Master::error('act_model_type_null');
		}
		if (!isset(self::$rightActTypes[$this->atype])) {
            Master::error('act_model_type_error'.$this->atype);
        }

		//基于活动类的存储
		$ActModel = Master::getAct($this->uid, $this->atype);
		if (!empty($ActModel->info)){
			$hd_data = $ActModel->info;
			$hd_data = $hd_data['tjson'];
		}

		if (empty($hd_data) || $hd_data['id'] != $hid){
			$hd_data['data'] = $this->do_init();
		}
		$this->hid = $hid;
		$this->info = $hd_data['data'];

		//构造输出结构体
		$this->make_out();
	}

	/*
	 * 构造输出结构体
	 */
	public function do_init(){
		return $this->_init;
	}

	/*
	 * 构造输出结构体
	 */
	public function make_out(){
		//默认输出直接等于内部存储数据
		$this->outf = $this->info;
	}

	/*
	 * 保存操作
	 */
    public function _save(){
        //基于活动类的存储
        $ActModel = Master::getAct($this->uid,$this->atype);
        $ActModel->setAct($this->atype,array(
            'id'=>$this->hid,
            'data' => $this->info,
        ));
    }

	public function save(){
		//基于活动类的存储
		$this->_save();
        $this->_update = true;
		$this->make_out();
	}

	/*
	 * 获取活动信息
	 */
	public function get_outf(){
		return $this->outf;
	}

	/*
	 * 返回活动信息
	 */
	public function back_data(){
		Master::back_data($this->uid,$this->b_mol,$this->b_ctrl,$this->outf);
	}

	/*
	 * 返回活动更新信息
	 * 如果活动需要分块返回更新信息 则改写这个函数
	 */
	public function back_data_u(){
		//默认调用返回所有信息
		$this->back_data();
	}


