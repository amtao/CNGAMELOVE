	/*
	 * 后端改造配置 key ::活动公用 不分玩家
	 */
	public function get_hdcfgKey(){
		return 'hdcfgKey_'.$this->atype.'_'.$this->hid;
	}

	/*
	 * 构造后端改造配置
	 */
	public function make_php_cfg(){
		//默认无改造
		return $this->hd_cfg;
	}

    /**
     * 设置今日红点时间
     */
    public function setDayNews()
    {
        if (in_array($this->atype, self::$_dayNewsTypes)) {
            $this->info['_day_news'] = date("Ymd");
            $this->save();
        }
    }

    /**
     * 红点处理
     * @return int 0:有红点，1:没有红点
     */
    public function get_news()
    {
        if (in_array($this->atype, self::$_dayNewsTypes)) {
            //第一次红点
            if (empty($this->info['_day_news']) || $this->info['_day_news'] != date("Ymd")) {
                return 1;
            }
        }
        return 0;
    }

	/*
	 * crontab - distroy()
	 * 后台脚本 即时写入
	 */
	public function ht_destroy(){
		$ActModel = Master::getAct($this->uid, $this->atype);
		$ActModel->destroy();
	}

